#include <unistd.h>
#include <stdio.h>
#include <stdlib.h>

int	main(void)
{
	int pipefd[2];
	int	pid;

	pipe(pipefd);
	pid = fork();
	if (pid == 0)
	{
		close(pipefd[0]);
		dup2(pipefd[1], 1);
		close(pipefd[1]);
	}
}